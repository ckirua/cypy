def __getattr__(name: str):
    """Raise with migration hint for removed Strategy B soft aliases."""
    from .compat import soft_alias_removal_hint

    hint = soft_alias_removal_hint(name)
    if hint is not None:
        raise AttributeError(hint)
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")


